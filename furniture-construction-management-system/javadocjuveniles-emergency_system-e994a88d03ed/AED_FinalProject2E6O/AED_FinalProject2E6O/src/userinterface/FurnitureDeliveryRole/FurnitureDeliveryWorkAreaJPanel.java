    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable2.getModel();

        model.setRowCount(0);

        for (WorkRequest request : furnitureDeliveryOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[11];
            row[0] = request;
            row[1] = request.getOrder();
            row[2] = request.getPhoneNumber();
            row[3] = request.getAddress();
            row[4] = request.getRemarks();
            row[5] = request.getEstimatedDelivery();
            row[6] = request.getModeofDelivery();
            row[7] = request.getDeliveryType();
            row[8] = request.getSender().getEmployee().getName();
            row[9] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();;
            row[10] = request.getStatus();

            model.addRow(row);
        }
    }

